class Solution {
public:
    int maxEnvelopes(vector<vector<int>>& envelopes) {
        int n = envelopes.size();
        if(n == 0) return 0;
        sort(envelopes.begin(), envelopes.end(), [](const vector<int>& a, const vector<int>& b) { 
            if(a[0] != b[0]) return a[0] < b[0];
            else return a[1] < b[1];
        });

        

        vector<int> dp(n+1, 0);
        for(int i = 0; i < n; ++i) {
            int max = 0;
            int w = envelopes[i][0];
            int h = envelopes[i][1];

            for(int j = 0; j < i; ++j) {
                int ww = envelopes[j][0];
                int hh = envelopes[j][1];
                if( ww < w && hh < h ) {
                    max = max > dp[j+1] ? max : dp[j+1];
                }
            }
            dp[i+1] = max + 1;
        }

        int res = 0;
        for(auto i : dp) {
            res = res > i ? res : i;
        }
        return res;

    }
};
