class Solution {
private:
    int mae(int a, int b) { 
        return a - b > 0 ? a - b : b - a;
    }

public:
    vector<vector<int>> allCellsDistOrder(int R, int C, int r0, int c0) {
        multimap<int, int> dist;

        for(int i = 0; i < R; ++i ) {
            for( int j = 0; j < C; ++j ) {
                int d = mae(r0, i) + mae(c0, j);
                dist.emplace(d, i*C+j);
            }
        }

        vector<vector<int>> res;
        for(auto it = dist.begin(); it != dist.end(); ++it) {
            int idx = it->second;
            int r = idx / C;
            int c = idx % C;

            vector<int> rr { r, c };
            res.push_back(rr);
        }
        return res;
    }
};
